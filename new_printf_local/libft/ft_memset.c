#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*b_cpy;
	size_t	i;

	i = 0;
	b_cpy = b;
	while (i < len)
	{
		*(b_cpy + i) = c;
		i++;
	}
	return (b);
}
